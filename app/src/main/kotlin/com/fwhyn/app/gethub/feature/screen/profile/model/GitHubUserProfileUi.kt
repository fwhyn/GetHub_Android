            starredUrl: String = "",
            subscriptionsUrl: String = "",
            organizationsUrl: String = "",
            reposUrl: String = "",
            eventsUrl: String = "",
            receivedEventsUrl: String = "",
            type: String = "",
            userViewType: String = "",
            siteAdmin: Boolean = false,
            name: String = "",
            company: String? = null,
            blog: String? = null,
            location: String? = null,
            email: String? = null,
            hireable: Boolean? = null,
            bio: String? = null,
            twitterUsername: String? = null,
            publicRepos: Int = 0,
            publicGists: Int = 0,
            followers: Int = 0,
            following: Int = 0,
            createdAt: String = "",
            updatedAt: String = "",
        ): GitHubUserProfileUi {
            return GitHubUserProfileUi(
                login = login,
                id = id,
                nodeId = nodeId,
                avatarUrl = avatarUrl,
                gravatarId = gravatarId,
                url = url,
                htmlUrl = htmlUrl,
                followersUrl = followersUrl,
                followingUrl = followingUrl,
                gistsUrl = gistsUrl,
                starredUrl = starredUrl,
                subscriptionsUrl = subscriptionsUrl,
                organizationsUrl = organizationsUrl,
                reposUrl = reposUrl,
                eventsUrl = eventsUrl,
                receivedEventsUrl = receivedEventsUrl,
                type = type,
                userViewType = userViewType,
                siteAdmin = siteAdmin,
                name = name,
                company = company,
                blog = blog,
                location = location,
                email = email,
                hireable = hireable,
                bio = bio,
                twitterUsername = twitterUsername,
                publicRepos = publicRepos,
                publicGists = publicGists,
                followers = followers,
                following = following,
                createdAt = createdAt,
                updatedAt = updatedAt
            )
        }
    }
}

