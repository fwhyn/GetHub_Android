    val publicGists: Int,
    val followers: Int,
    val following: Int,
    val createdAt: String,
    val updatedAt: String,
) {
    companion object {
        fun default(
            login: String = "",
            id: Int = 0,
            nodeId: String = "",
            avatarUrl: String = "",
            gravatarId: String = "",
            url: String = "",
            htmlUrl: String = "",
            followersUrl: String = "",
